#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tra;
	size_t			i;

	tra = s;
	i = 0;
	while (i < n)
	{
		tra[i] = '\0';
		i++;
	}
	return (0);
}
