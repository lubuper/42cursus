#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (s[i])
	{
		i++;
		str++;
	}
	while (i >= 0)
	{
		if ((unsigned char)s[i] == (unsigned char)c)
			return (str);
		i--;
		str--;
	}
	return (NULL);
}
