#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

int	main(void)
{
	{
		Weapon	club = Weapon("rusty nails club");
		HumanA	bob("Bob", club);
		bob.attack();
		club.setType("bloody bent nails club");
		bob.attack();
	}
	{
		Weapon	club = Weapon("crude spiked club");
		HumanB	jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("broken club");
		jim.attack();
	}

	return 0;
}