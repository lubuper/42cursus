#include <stdio.h>

char	*ft_strcpy(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = 0;
	return (s2);
}

int	main(void)
{
	char	str[] = "test";
	char	dst[] = "";
	
	ft_strcpy(str, dst);
	printf("%s\n", dst);
	return (0);
}
