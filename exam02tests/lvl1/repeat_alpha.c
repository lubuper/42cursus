#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	sum;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if (av[1][i] >= 97 && av[1][i] <= 122)
				sum = av[1][i] - 96;
			else if (av[1][i] >= 65 && av[1][i] <= 90)
				sum = av[1][i] - 64;
			else
				write (1, &av[1][i], 1);
			while (sum > 0)
			{
				write (1, &av[1][i], 1);
				sum--;
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
