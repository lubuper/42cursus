void	ft_swap(int *a, int *b)
{
	int	i;
	int	*temp;

	temp = 0;
	i = 0;

	while (a[i])
	{
		temp[i] = a[i];
		a[i] = b[i];
		b[i] = temp[i];
		i++;
	}
}
