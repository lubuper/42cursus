#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*top;

	top = s;
	i = 0;
	while (i < n)
	{
		top[i] = c;
		i++;
	}
	return (s);
}
